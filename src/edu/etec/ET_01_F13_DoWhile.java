package edu.etec;

public class ET_01_F13_DoWhile {

    public static void main(String[] args) {

        // variable declaration
        byte i = 0;

        // flow repetition
        do {
            System.out.println(i + " ");
            i++;
        } while (i <= 10);
        System.out.println("\nAcabou");
    }

}
