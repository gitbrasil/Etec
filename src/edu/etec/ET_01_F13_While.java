package edu.etec;

public class ET_01_F13_While {

    public static void main(String[] args) {

        // variable declaration
        byte i = 0;

        // controlar fluxo
        while (i <= 10) {
            System.out.println(i + " ");
            i++;
        }
        System.out.println("\nAcabou");
    }

}
